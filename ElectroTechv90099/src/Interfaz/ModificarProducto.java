    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try{
            
            Producto p = new Producto();
            p.setId(Integer.parseInt(this.txtShowId.getText()));
            p.buscarID();
            
            if(ConexionBD.buscarID){
                String nombre = this.txtShowNombre.getText();
                String marca = this.txtShowMarca.getText();
                String categoria = this.txtcat2.getSelectedItem().toString(); 
                String precioTexto = this.txtShowPrecio.getText();
                String stockTexto = this.txtShowStock.getText();
                String fechaTexto = this.txtShowFecha.getText();
                if(nombre.isEmpty() || marca.isEmpty() || categoria.isEmpty() || precioTexto.isEmpty() || stockTexto.isEmpty() || fechaTexto.isEmpty()){
                    JOptionPane.showMessageDialog(this, "Se deben completar todos los campos", "Completar todos los campos", 2);
                }else{
                    try{
                        int precio = Integer.parseInt(precioTexto);
                        int stock = Integer.parseInt(stockTexto);
                        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                        Date fecha_final = sdf.parse(fechaTexto);

                        p.setNombre(nombre);
                        p.setMarca(marca);
                        p.setCategoria(categoria);
                        p.setPrecio(precio);
                        p.setStock(stock);
                        p.setFecha(fecha_final);

                       if(p.validarProducto(nombre, p.getId()) ){
                            JOptionPane.showMessageDialog(this, "El nombre del producto ya existe", "Error", 2);
                            
                        }else{
                           int opcion = JOptionPane.showConfirmDialog(this, "Quieres actualizar el producto?","Actualizar",JOptionPane.YES_NO_OPTION,2);
                
                            if (opcion== JOptionPane.YES_OPTION){
                                p.actualizarProducto();
                                JOptionPane.showMessageDialog(this, "Producto Actualizado","Actualizar",2);
                                limpiar();
                            }else{
                                JOptionPane.showMessageDialog(this, "Producto no actualizado","Actualizar",2);
                                limpiar();
                            } 
                        }
                    }catch (NumberFormatException e){
                        JOptionPane.showMessageDialog(this, "Los campos año y duración deben ser números", "Completar Año y Duración", 2);
                    }
                }
            }else{
                JOptionPane.showMessageDialog(this, "Producto no actualizado.","ATENCION",2);
                limpiar();
            }    
        }catch(Exception e){
            System.out.println("Error al actualizar");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

