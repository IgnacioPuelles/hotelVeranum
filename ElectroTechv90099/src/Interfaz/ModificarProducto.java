    private void btnBuscarIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarIDActionPerformed
        try{
            Producto p = new Producto();
            p.setId(Integer.parseInt(this.txtBuscarID.getText()));
            p.buscarID();
            if(ConexionBD.buscarID){
                this.txtShowId.setText(String.valueOf(p.getId()));
                this.txtShowNombre.setText(p.getNombre());
                this.txtShowMarca.setText(p.getMarca());
                this.txtcat2.setSelectedItem(String.valueOf(p.getCategoria()));
                this.txtShowPrecio.setText(String.valueOf(p.getPrecio()));
                this.txtShowStock.setText(String.valueOf(p.getStock()));
                SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
                String fecha1 = formatoFecha.format(p.getFecha());
                this.txtShowFecha.setText(fecha1);
            }else{
                JOptionPane.showMessageDialog(this, "Producto no encontrado","ATENCION",2);
            }

        }catch(Exception e){
            System.out.println("Error al buscar ID");
        }
    }//GEN-LAST:event_btnBuscarIDActionPerformed

